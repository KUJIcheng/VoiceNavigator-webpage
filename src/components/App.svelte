<script>
  import { onMount, tick } from "svelte";

  let agents = ["greeter", "OCR", "selfOperating"];

  // Âä®ÊÄÅÂ§ßÂ∞èÔºàÂü∫‰∫éÁ™óÂè£ÂÆΩÂ∫¶Ôºâ
  let userSize = "8vw";  // User Â§ßÂ∞è
  let agentSize = "6vw"; // Agent Â§ßÂ∞è
  let radius = "14vw";   // ÊóãËΩ¨ÂçäÂæÑ

  const positions = [
    { bottom: "10%", left: "46%", transform: `translate(-${radius}, 0) scale(1)` }, // Â∑¶‰∏ãËßí
    { bottom: "50%", left: "50%", transform: `translateX(-50%) scale(1.5)` },   // Ê≠£‰∏ãÊñπÔºàÊîæÂ§ßÔºâ
    { bottom: "10%", left: "46%", transform: `translate(${radius}, 0) scale(1)` }  // Âè≥‰∏ãËßí
  ];

  // ÂàùÂßã‰ΩçÁΩÆÔºögreeter(‰∏≠Èó¥) -> OCR(Â∑¶‰∏ã) -> selfOperating(Âè≥‰∏ã)
  let order = [1, 0, 2];

  // ÂΩìÂâçÂú® user ‰∏ãÊñπÁöÑ Agent
  let activeAgentIndex = 0;

  // ÊòØÂê¶Ëá™Âä®ËΩÆÊç¢
  let autoRotate = true;
  let rotateInterval;

  // È°µÈù¢ÊåÇËΩΩÂêéÔºåÊØè 3 ÁßíËΩÆÊç¢
  onMount(() => {
    rotateInterval = setInterval(rotateAgents, 3000);
    return () => clearInterval(rotateInterval);
  });

  async function rotateAgents() {
    if (!autoRotate) return;

    setTimeout(async () => {
      const last = order.pop();
      order.unshift(last);
      order = [...order]; // Ëß¶ÂèëÂìçÂ∫îÂºè
      activeAgentIndex = order.findIndex(posIndex => posIndex === 1);
      await tick();
    }, 50);
  }

  async function switchAgent(agent) {
    let index = agents.indexOf(agent);
    if (index === -1 || index === activeAgentIndex) return;

    autoRotate = false;

    let safetyCounter = 0;
    while (order[index] !== 1 && safetyCounter < 6) {
      const last = order.pop();
      order.unshift(last);
      order = [...order];

      await tick();
      safetyCounter++;
    }

    activeAgentIndex = order.findIndex(posIndex => posIndex === 1);

    setTimeout(() => {
      autoRotate = true;
    }, 3000);
  }

  // ÁõëÂê¨Á™óÂè£Â§ßÂ∞èÂèòÂåñÔºåÂä®ÊÄÅË∞ÉÊï¥Â§ßÂ∞è
  function updateSizes() {
    const width = window.innerWidth;
    userSize = `${Math.max(60, width * 0.08)}px`;
    agentSize = `${Math.max(50, width * 0.05)}px`;
    radius = `${Math.max(100, width * 0.05)}px`;
  }

  onMount(() => {
    updateSizes();
    window.addEventListener("resize", updateSizes);
    return () => window.removeEventListener("resize", updateSizes);
  });

  // ÁõëÂê¨ÊªöÂä® & activeSection
  let activeSection = "intro";
  onMount(() => {
    function handleScroll() {
      const sections = document.querySelectorAll("section");
      let currentSection = "intro";

      sections.forEach((section) => {
        const rect = section.getBoundingClientRect();
        if (
          rect.top <= window.innerHeight * 0.5 &&
          rect.bottom >= window.innerHeight * 0.5
        ) {
          currentSection = section.id;
        }
      });
      activeSection = currentSection;
    }
    window.addEventListener("scroll", handleScroll);
    return () => window.removeEventListener("scroll", handleScroll);
  });

  function navigateTo(section) {
    document.getElementById(section).scrollIntoView({ behavior: "smooth" });
  }
</script>

